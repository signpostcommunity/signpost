/* ── Main Component ── */

interface HowItWorksProps {
  role: 'interpreter' | 'deaf' | 'requester'
  onContinue: () => void
}

export default function HowItWorks({ role, onContinue }: HowItWorksProps) {
  const isDeaf = role === 'deaf'
  const accentColor = isDeaf ? '#a78bfa' : '#00e5ff'

  const steps = role === 'interpreter'
    ? INTERPRETER_STEPS
    : role === 'deaf'
    ? DEAF_STEPS
    : REQUESTER_STEPS

  const content = role === 'interpreter'
    ? INTERPRETER_CONTENT
    : role === 'deaf'
    ? DEAF_CONTENT
    : REQUESTER_CONTENT

  return (
    <div style={{ padding: '48px 0' }}>
      <StepDiagram steps={steps} accentColor={accentColor} />

      <div style={{ maxWidth: 680, margin: '0 auto' }}>
        <h2 style={{
          fontFamily: "'Syne', sans-serif",
          fontWeight: 700,
          fontSize: 20,
          color: '#f0f2f8',
          letterSpacing: '-0.01em',
          marginBottom: 28,
        }}>
          {content.heading}
        </h2>

        <div style={{ display: 'flex', flexDirection: 'column', gap: 16 }}>
          {content.blocks.map((block, i) => (
            <div key={i}>
              <div style={{
                fontFamily: "'Syne', sans-serif",
                fontWeight: 600,
                fontSize: 15,
                color: '#f0f2f8',
                marginBottom: 6,
              }}>
                {block.bold}
              </div>
              <div style={{
                fontWeight: 400,
                fontSize: 14,
                color: '#96a0b8',
                lineHeight: 1.65,
              }}>
                {block.body}
              </div>
            </div>
          ))}
        </div>

        <button
          onClick={onContinue}
          className="btn-primary btn-large"
          style={{
            marginTop: 36,
            width: '100%',
            padding: '14px 24px',
            background: accentColor,
            color: '#0a0a0f',
          }}
        >
          {content.cta}
        </button>
      </div>
    </div>
  )
}
